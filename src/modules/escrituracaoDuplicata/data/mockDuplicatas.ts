const pad = (n: number, len: number) => String(n).padStart(len, '0');

// Converte um deslocamento de dias a partir de 01/01/2026 em dd/mm/aaaa.
const dataDe = (offsetDias: number): string => {
  const base = new Date(2026, 0, 1);
  base.setDate(base.getDate() + offsetDias);
  return `${pad(base.getDate(), 2)}/${pad(base.getMonth() + 1, 2)}/${base.getFullYear()}`;
};

function gerarVolume(quantidade: number, idInicial: number): Base[] {
  const out: Base[] = [];
  for (let i = 0; i < quantidade; i++) {
    const seq = idInicial + i;
    const status = STATUS_CICLO[i % STATUS_CICLO.length];
    const cliente = CLIENTES[i % CLIENTES.length];
    const tipo = TIPOS[i % TIPOS.length];
    const forma = FORMAS[i % FORMAS.length];
    const origem: Base['origem'] = i % 3 === 0 ? 'manual' : 'automatica';

    const numeroStr = pad(1000 + seq, 6);
    const parcelaAtual = (i % 4) + 1;
    const parcelaTotal = parcelaAtual + (i % 3);
    const valor = 750 + ((i * 631) % 49000);

    const criadaDia = i % 150;
    const criadaEm = dataDe(criadaDia);
    const registradaEm = dataDe(criadaDia + 1);
    const vencimento = dataDe(criadaDia + 30 + (i % 40));

    const d: Base = {
      id: `g-${pad(seq, 4)}`,
      cliente,
      tipo,
      numero: `${numeroStr}-${pad(parcelaAtual, 2)}`,
      valor,
      vencimento,
      origem,
      status,
      parcela: `${parcelaAtual} de ${parcelaTotal}`,
      notaOrigem: `NF ${numeroStr}`,
      formaPagamento: forma,
      criadaEm,
    };

    if (status === 'ativa') {
      d.registradaEm = registradaEm;
    } else if (status === 'aguardando') {
      d.motivoPendencia = MOTIVOS_PENDENCIA_GEN[i % MOTIVOS_PENDENCIA_GEN.length];
    } else if (status === 'negociada') {
      d.registradaEm = registradaEm;
      d.negociadaEm = dataDe(criadaDia + 18);
    } else if (status === 'paga') {
      d.registradaEm = registradaEm;
      d.pagaEm = dataDe(criadaDia + 25);
    } else if (status === 'cancelada') {
      if (i % 2 === 0) d.registradaEm = registradaEm;
      d.motivoCancelamento = MOTIVOS_CANCELAMENTO_GEN[i % MOTIVOS_CANCELAMENTO_GEN.length];
      d.canceladaEm = dataDe(criadaDia + 10);
    }

    // Crédito vinculado (cessão/ônus) em ~1 a cada 4 duplicatas elegíveis.
    if ((status === 'ativa' || status === 'negociada' || status === 'paga') && i % 4 === 0) {
      const fin = FINANCIADORES_GEN[i % FINANCIADORES_GEN.length];
      const isCessao = i % 8 < 4;
      const protocolo = ((seq * 2654435761) >>> 0).toString(16).toUpperCase().padStart(8, '0').slice(0, 8);
      d.creditoVinculado = {
        tipo: isCessao ? 'cessao' : 'onus',
        financiador: fin.nome,
        financiadorCnpj: fin.cnpj,
        valorEfeito: isCessao ? valor : Math.round(valor * 0.8),
        registradora: 'CERC',
        contrato: `CTR-2026-${pad(seq, 5)}`,
        protocoloCerc: `CERC-${protocolo}`,
        registradoEm: dataDe(criadaDia + 2),
        ...(isCessao ? {} : { validade: vencimento }),
      };
    }

    out.push(d);
  }
  return out;
}

