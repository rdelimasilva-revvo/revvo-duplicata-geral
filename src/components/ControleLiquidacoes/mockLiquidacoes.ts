const pad = (n: number, width: number) => String(n).padStart(width, '0');
const round2 = (n: number) => Math.round(n * 100) / 100;

function gerarLiquidacao(i: number): Omit<Liquidacao, 'sacadorId'> {
  const cliente = CLIENTES_POOL[i % CLIENTES_POOL.length];
  const banco = LIQUIDANTES_POOL[i % LIQUIDANTES_POOL.length];
  const negociacao: NegociacaoTipo = i % 3 === 0 ? 'onus' : 'cessao';
  const status = STATUS_CICLO[i % STATUS_CICLO.length];
  const seq = 600 + i;
  const numDup = pad(seq, 6);
  const valor = round2(800 + ((i * 173) % 49000));
  const valorCedido = negociacao === 'onus' ? round2(valor * 0.7) : valor;
  const parcelaTotal = (i % 4) + 1;
  const parcelaAtual = (i % parcelaTotal) + 1;
  const notificado = i % 5 !== 0;

  const l: Omit<Liquidacao, 'sacadorId'> = {
    id: `liq-${pad(46 + i, 4)}`,
    duplicataNumero: `${numDup}-${pad(parcelaAtual, 2)}`,
    tipoDuplicata: TIPOS[i % TIPOS.length],
    notaOrigem: `NF ${numDup}`,
    parcela: `${parcelaAtual} de ${parcelaTotal}`,
    valor,
    vencimento: VENCIMENTOS[i % VENCIMENTOS.length],
    formaPagamento: FORMAS[i % FORMAS.length],
    cliente: cliente.nome,
    clienteCnpj: cliente.cnpj,
    negociacao,
    registradora: 'CERC',
    contrato: `CTR-2026-${pad(seq, 5)}`,
    protocoloCerc: `CERC-${pad(seq, 6)}${pad((i * 7) % 100, 2)}`,
    registradoEm: REGISTRADOS[i % REGISTRADOS.length],
    liquidante: banco.nome,
    liquidanteCnpj: banco.cnpj,
    valorCedido,
    status,
    valorLiquidado: 0,
    clienteNotificado: notificado,
  };

  if (notificado) l.notificadoEm = REGISTRADOS[(i + 1) % REGISTRADOS.length];

  if (status === 'parcial') {
    l.valorLiquidado = round2(valorCedido * 0.4);
    l.ultimoPagamentoEm = PAGAMENTOS[i % PAGAMENTOS.length];
  } else if (status === 'liquidada') {
    l.valorLiquidado = valorCedido;
    l.liquidadaEm = PAGAMENTOS[i % PAGAMENTOS.length];
    l.ultimoPagamentoEm = l.liquidadaEm;
    const baixa = BAIXAS[i % BAIXAS.length];
    l.baixaRegistradora = baixa;
    if (baixa !== 'pendente') {
      l.baixaReportadaEm = PAGAMENTOS[(i + 1) % PAGAMENTOS.length];
      // Padrão é automático; parte é manual (double check feito por operador).
      l.baixaModo = i % 4 === 0 ? 'manual' : 'automatico';
    }
  } else if (status === 'em_atraso') {
    const parcial = i % 2 === 0;
    l.valorLiquidado = parcial ? round2(valorCedido * 0.3) : 0;
    if (parcial) l.ultimoPagamentoEm = PAGAMENTOS[i % PAGAMENTOS.length];
    l.diasAtraso = (i % 25) + 1;
  }

  return l;
}

